// Объявление функции с двумя аргументами. Первый аргмумент - цифра, которую фонкция возводит в степень.
// Второй аргумент - в какую степень нужно возвести. Возвращает результат возведения числа в степерь.
const power = function(base, exponent) {
    let result = 1;
    for (let count = 0; count < exponent; count++) {
        result *= base;
    }
    return result;
}
// Вызво фнкции и вывод в консоль результата работы функции.
console.log(power(2,11));

