const hummus = function(factor) {
    // считает и выводит в консоль сообщение с элементом рецепта хумуса и его колчисеством.
    const ingredient = function(amount, unit, name) {
        let ingredientAmount = amount * factor;
        if (ingredientAmount > 1) {
            unit += "s";
        }
        console.log(`\t${ingredientAmount} ${unit} ${name}`);
    }
    console.log(`Вот что нужно для приготовления ${factor} порций хумуса:`)
    ingredient(1, "банка", "нута");
    ingredient(0.25, "стакан", "кунжутной пасты");
    ingredient(0.25, "стакан", "лимонного сока");
    ingredient(1, "зубок", "чеснока");
    ingredient(2, "столовые ложки", "оливкового масла");
    ingredient(0.5, "чайные ложки", "кумина");
 }

